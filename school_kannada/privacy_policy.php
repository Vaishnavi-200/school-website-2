                        <h4>
                           ಈ ವೆಬ್‌ಸೈಟ್ ಬಳಸುವಾಗ ನೀವು ಒದಗಿಸುವ ಯಾವುದೇ ಮಾಹಿತಿಯನ್ನು ವಿವಾ ಗುಂಪು ಹೇಗೆ ಬಳಸುತ್ತದೆ ಮತ್ತು ರಕ್ಷಿಸುತ್ತದೆ ಎಂಬುದನ್ನು ಈ ಗೌಪ್ಯತೆ ನೀತಿಯು ತೋರಿಸುತ್ತದೆ. ನಿಮ್ಮ ಗೌಪ್ಯತೆಯನ್ನು ರಕ್ಷಿಸಲು ನಾವು ಬದ್ಧರಾಗಿರುತ್ತೇವೆ . ನಿಮ್ಮಿಂದ ಸಂಗ್ರಹಿಸಲಾದ ಎಲ್ಲಾ ಮಾಹಿತಿಯು ಈ ಗೌಪ್ಯತೆ ನೀತಿಗೆ ಒಳಪಟ್ಟಿರುತ್ತದೆ.<br><br><br>
                           ನಾವು ಸಂಗ್ರಹಿಸುವ ಮಾಹಿತಿ
                           <br><br><br>
                           1). ಸಂಪರ್ಕ ಮಾಹಿತಿ: ನಾವು ನಿಮ್ಮ ಹೆಸರು, ಇಮೇಲ್, ಮೊಬೈಲ್ ಸಂಖ್ಯೆ, ಫೋನ್ ಸಂಖ್ಯೆ, ರಸ್ತೆ, ನಗರ, ರಾಜ್ಯ, ಪಿನ್ ಕೋಡ್, ದೇಶ ಮತ್ತು IP ವಿಳಾಸವನ್ನು ಸಂಗ್ರಹಿಸಬಹುದು.<br><br>
                           2). ನೀವು ಪೋಸ್ಟ್ ಮಾಡುವ ಮಾಹಿತಿ: ನಮ್ಮ ವೆಬ್‌ಸೈಟ್‌ನಲ್ಲಿ ಅಥವಾ ನಮಗೆ ಸೇರಿದ ಮೂರನೇ ವ್ಯಕ್ತಿಯ ಸಾಮಾಜಿಕ ಮಾಧ್ಯಮ ಸೈಟ್‌ನಲ್ಲಿ ಸಾರ್ವಜನಿಕ ಸ್ಥಳದಲ್ಲಿ ನೀವು ಪೋಸ್ಟ್ ಮಾಡುವ ಮಾಹಿತಿಯನ್ನೂ  ನಾವು ಸಂಗ್ರಹಿಸುತ್ತೇವೆ.<br><br>
                           3).ಜನಸಂಖ್ಯಾ ಮಾಹಿತಿ: ನಮ್ಮ ವೆಬ್‌ಸೈಟ್‌ನ ಬಳಕೆಯ ಸಮಯದಲ್ಲಿ ನಿಮ್ಮ ಬಗ್ಗೆ ಜನಸಂಖ್ಯಾ ಮಾಹಿತಿಯನ್ನು ಅಥವಾ ನೀವು ಒದಗಿಸಿದ ಯಾವುದೇ ಮಾಹಿತಿಯನ್ನು ನಾವು ಸಂಗ್ರಹಿಸಬಹುದು. ನಾವು ಇದನ್ನು ಸಮೀಕ್ಷೆಯ ಭಾಗವಾಗಿ ಸಂಗ್ರಹಿಸಬಹುದು.<br><br>
                           4) ಇತರ ಮಾಹಿತಿ: ನೀವು ನಮ್ಮ ವೆಬ್‌ಸೈಟ್ ಅನ್ನು ಬಳಸಿದರೆ, ನಿಮ್ಮ IP ವಿಳಾಸ ಮತ್ತು ನೀವು ಬಳಸುತ್ತಿರುವ ಬ್ರೌಸರ್ ಕುರಿತು ನಾವು ಮಾಹಿತಿಯನ್ನು ಸಂಗ್ರಹಿಸಬಹುದು. ನೀವು ಯಾವ ಸೈಟ್‌ನಿಂದ ಬಂದಿದ್ದೀರಿ, ನಮ್ಮ ವೆಬ್‌ಸೈಟ್‌ನಲ್ಲಿ ಕಳೆದ ಸಮಯದ ಅವಧಿ, ಪ್ರವೇಶಿಸಿದ ಪುಟಗಳು ಅಥವಾ ನೀವು ನಮ್ಮನ್ನು ತೊರೆದಾಗ ನೀವು ಯಾವ ಸೈಟ್‌ಗೆ ಭೇಟಿ ನೀಡುತ್ತೀರಿ ಎಂಬುದನ್ನು ನಾವು ನೋಡಬಹುದು. ನೀವು ಬಳಸುತ್ತಿರುವ ಮೊಬೈಲ್ ಸಾಧನದ ಪ್ರಕಾರ ಅಥವಾ ನಿಮ್ಮ ಕಂಪ್ಯೂಟರ್ ಅಥವಾ ಸಾಧನ ಚಾಲನೆಯಲ್ಲಿರುವ ಆಪರೇಟಿಂಗ್ ಸಿಸ್ಟಂನ ಆವೃತ್ತಿಯನ್ನು ಸಹ ನಾವು ಸಂಗ್ರಹಿಸಬಹುದು.<br><br>
                           5) ನಾವು ನಿಮ್ಮಿಂದ ನಿಷ್ಕ್ರಿಯವಾಗಿ ಮಾಹಿತಿಯನ್ನು ಸಂಗ್ರಹಿಸುತ್ತೇವೆ. ನಮ್ಮ ವೆಬ್‌ಸೈಟ್‌ನ ನಿಮ್ಮ ಬಳಕೆಯ ಕುರಿತು ಮಾಹಿತಿಯನ್ನು ಸಂಗ್ರಹಿಸಲು ನಾವು ಗೂಗಲ್ ಅನಾಲಿಟಿಕ್ಸ್, ಬ್ರೌಸರ್ ಕುಕೀಗಳು ಮತ್ತು ಇತರ ಭದ್ರತಾ ಪರಿಕರಗಳಂತಹ ಟ್ರ್ಯಾಕಿಂಗ್ ಪರಿಕರಗಳನ್ನು ಬಳಸುತ್ತೇವೆ.<br><br>
                           6) ನಾವು ಮೂರನೇ ವ್ಯಕ್ತಿಗಳಿಂದ ನಿಮ್ಮ ಬಗ್ಗೆ ಮಾಹಿತಿಯನ್ನು ಪಡೆಯುತ್ತೇವೆ. ಮೂರನೇ ವ್ಯಕ್ತಿಯ ಸಾಮಾಜಿಕ ಮಾಧ್ಯಮ ಸೈಟ್‌ನಿಂದ ನಾವು ನಿಮ್ಮ ಬಗ್ಗೆ ಮಾಹಿತಿಯನ್ನು ಪಡೆಯಬಹುದು. ಇದು ನಿಮ್ಮ ಹೆಸರು, ಇಮೇಲ್ ವಿಳಾಸ ಮತ್ತು ಮೊಬೈಲ್ ಸಂಖ್ಯೆಯನ್ನು ಒಳಗೊಂಡಿರಬಹುದು.<br><br><br>
                           ನಿಮ್ಮ ವೈಯಕ್ತಿಕ ಮಾಹಿತಿಯ ಬಳಕೆ
                           <br><br><br><br>
                           1) ನಿಮ್ಮನ್ನು ಸಂಪರ್ಕಿಸಲು ನಾವು ಮಾಹಿತಿಯನ್ನು ಬಳಸುತ್ತೇವೆ. ಪ್ರಚಾರದ ಉದ್ದೇಶಗಳಿಗಾಗಿ ನಿಮ್ಮನ್ನು ಸಂಪರ್ಕಿಸಲು ನೀವು ಒದಗಿಸಿದ ಮಾಹಿತಿಯನ್ನು ನಾವು ಬಳಸಬಹುದು.
                           <br><br>
                           2) ನಿಮ್ಮ ವಿನಂತಿಗಳು ಅಥವಾ ಪ್ರಶ್ನೆಗಳಿಗೆ ಪ್ರತಿಕ್ರಿಯಿಸಲು ನಾವು ಮಾಹಿತಿಯನ್ನು ಬಳಸುತ್ತೇವೆ. ನೀವು ನಮ್ಮ ಯಾವುದೇ ಕೋರ್ಸ್‌ಗಳಿಗೆ ಆನ್‌ಲೈನ್‌ನಲ್ಲಿ ಅರ್ಜಿ ಸಲ್ಲಿಸಿದ್ದರೆ ನಿಮ್ಮ ಅರ್ಜಿಯನ್ನು ದೃಢೀಕರಿಸಲು ನಿಮ್ಮ ಮಾಹಿತಿಯನ್ನು ನಾವು ಬಳಸಬಹುದು.
                           <br><br>
                           3) ನಮ್ಮ ಪ್ರವೇಶ ಸೇವೆಗಳನ್ನು ಸುಧಾರಿಸಲು ನಾವು ಮಾಹಿತಿಯನ್ನು ಬಳಸುತ್ತೇವೆ. ನಮ್ಮೊಂದಿಗೆ ನಿಮ್ಮ ಅನುಭವವನ್ನು ಕಸ್ಟಮೈಸ್ ಮಾಡಲು ನಾವು ನಿಮ್ಮ ಮಾಹಿತಿಯನ್ನು ಬಳಸಬಹುದು. ಇದು ನಿಮ್ಮ ಆದ್ಯತೆಗಳ ಆಧಾರದ ಮೇಲೆ ವಿಷಯವನ್ನು ಪ್ರದರ್ಶಿಸುವುದನ್ನು ಒಳಗೊಂಡಿರಬಹುದು.
                           <br><br>
                           4) ಸೈಟ್ ಟ್ರೆಂಡ್‌ಗಳು ಮತ್ತು ಸಂದರ್ಶಕರ ಆಸಕ್ತಿಗಳನ್ನು ನೋಡಲು ನಾವು ಮಾಹಿತಿಯನ್ನು ಬಳಸುತ್ತೇವೆ. ನಮ್ಮ ವೆಬ್‌ಸೈಟ್ ಮತ್ತು ಸೇವೆಗಳನ್ನು ಉತ್ತಮಗೊಳಿಸಲು ನಿಮ್ಮ ಮಾಹಿತಿಯನ್ನು ನಾವು ಬಳಸಬಹುದು. ನಾವು ನಿಮ್ಮಿಂದ ಪಡೆಯುವ ಮಾಹಿತಿಯನ್ನು ನಾವು ಮೂರನೇ ವ್ಯಕ್ತಿಗಳಿಂದ ಪಡೆಯುವ ನಿಮ್ಮ ಬಗ್ಗೆ ಮಾಹಿತಿಯೊಂದಿಗೆ ಸಂಯೋಜಿಸಬಹುದು.
                           <br><br>
                           5) ನಾವು ಭದ್ರತಾ ಉದ್ದೇಶಗಳಿಗಾಗಿ ಮಾಹಿತಿಯನ್ನು ಬಳಸುತ್ತೇವೆ. ನಮ್ಮ ಸಂಸ್ಥೆ, ನಮ್ಮ ಮಾನವ ಸಂಪನ್ಮೂಲಗಳು ಅಥವಾ ನಮ್ಮ ವೆಬ್‌ಸೈಟ್‌ಗಳನ್ನು ರಕ್ಷಿಸಲು ನಾವು ಮಾಹಿತಿಯನ್ನು ಬಳಸಬಹುದು.<br><br><br>
                           6) ನಾವು ಮಾರ್ಕೆಟಿಂಗ್ ಉದ್ದೇಶಗಳಿಗಾಗಿ ಮಾಹಿತಿಯನ್ನು ಬಳಸುತ್ತೇವೆ.
                           <br><br>
                           7) ನಿಮಗೆ ವಹಿವಾಟಿನ ಸಂವಹನಗಳನ್ನು ಕಳುಹಿಸಲು ನಾವು ಮಾಹಿತಿಯನ್ನು ಬಳಸುತ್ತೇವೆ. ನಾವು ನಿಮಗೆ ಸಂಬಂಧಿತ ಇಮೇಲ್‌ಗಳು ಅಥವಾ SMS ಕಳುಹಿಸಬಹುದು.
                           <br><br>
                           8) ಕಾನೂನಿನ ಪ್ರಕಾರ ಅನುಮೋದಿಸಲಾದ ಮಾಹಿತಿಯನ್ನು ನಾವು ಬಳಸುತ್ತೇವೆ.
