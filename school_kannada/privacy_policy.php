                           1) ಕುಕೀಯು ನಿಮ್ಮ ಕಂಪ್ಯೂಟರ್‌ನ ಹಾರ್ಡ್ ಡ್ರೈವ್‌ನಲ್ಲಿ ಇರಿಸಲು ಅನುಮತಿ ಕೇಳುವ ಚಿಕ್ಕ ಫೈಲ್ ಆಗಿದೆ. ಒಮ್ಮೆ ನೀವು ಒಪ್ಪಿಕೊಂಡರೆ, ಫೈಲ್ ಅನ್ನು ಸೇರಿಸಲಾಗುತ್ತದೆ ಮತ್ತು ವೆಬ್ ಟ್ರಾಫಿಕ್ ಅನ್ನು ವಿಶ್ಲೇಷಿಸಲು ಕುಕೀ ಸಹಾಯ ಮಾಡುತ್ತದೆ ಅಥವಾ ನೀವು ನಿರ್ದಿಷ್ಟ ಸೈಟ್‌ಗೆ ಭೇಟಿ ನೀಡಿದಾಗ ನಿಮಗೆ ತಿಳಿಸುತ್ತದೆ. ಕುಕೀಗಳು ವೆಬ್ ಅಪ್ಲಿಕೇಶನ್‌ಗಳಿಗೆ ವೈಯಕ್ತಿಕವಾಗಿ ನಿಮಗೆ ಪ್ರತಿಕ್ರಿಯಿಸಲು ಅವಕಾಶ ಮಾಡಿಕೊಡುತ್ತವೆ. ನಿಮ್ಮ ಆದ್ಯತೆಗಳ ಬಗ್ಗೆ ಮಾಹಿತಿಯನ್ನು ಸಂಗ್ರಹಿಸುವ ಮತ್ತು ನೆನಪಿಟ್ಟುಕೊಳ್ಳುವ ಮೂಲಕ ವೆಬ್ ಅಪ್ಲಿಕೇಶನ್ ತನ್ನ ಕಾರ್ಯಾಚರಣೆಗಳನ್ನು ನಿಮ್ಮ ಅಗತ್ಯತೆಗಳು, ಇಷ್ಟಗಳು ಮತ್ತು ಇಷ್ಟಪಡದಿರುವಿಕೆಗಳಿಗೆ ತಕ್ಕಂತೆ ಮಾಡಬಹುದು. ಯಾವ ಪುಟಗಳನ್ನು ಬಳಸಲಾಗುತ್ತಿದೆ ಎಂಬುದನ್ನು ಗುರುತಿಸಲು ನಾವು ಟ್ರಾಫಿಕ್ ಲಾಗ್ ಕುಕೀಗಳನ್ನು ಬಳಸುತ್ತೇವೆ. ವೆಬ್‌ಪುಟದ ದಟ್ಟಣೆಯ ಕುರಿತು ಡೇಟಾವನ್ನು ವಿಶ್ಲೇಷಿಸಲು ಮತ್ತು ರೋಗಿಗಳ ಅಗತ್ಯಗಳಿಗೆ ತಕ್ಕಂತೆ ನಮ್ಮ ವೆಬ್‌ಸೈಟ್ ಅನ್ನು ಸುಧಾರಿಸಲು ಇದು ನಮಗೆ ಸಹಾಯ ಮಾಡುತ್ತದೆ. ನಾವು ಈ ಮಾಹಿತಿಯನ್ನು ಅಂಕಿಅಂಶಗಳ ವಿಶ್ಲೇಷಣೆ ಉದ್ದೇಶಗಳಿಗಾಗಿ ಮಾತ್ರ ಬಳಸುತ್ತೇವೆ ಮತ್ತು ನಂತರ ಡೇಟಾವನ್ನು ಸಿಸ್ಟಮ್‌ನಿಂದ ತೆಗೆದುಹಾಕಲಾಗುತ್ತದೆ.
                           <br><br>
                           2) ಒಟ್ಟಾರೆಯಾಗಿ,  ನೀವು ಯಾವ ಪುಟಗಳನ್ನು ಉಪಯೋಗಿಸುತಿದ್ದೀರಿ ಮತ್ತು ಯಾವ ಪುಟಗಳನ್ನೂ ನೀವು ಉಪಸಯೋಗಿಸುವುದಿಲ್ಲ ಎಂಬುವುದರ ಬಗ್ಗೆ ಮೇಲ್ವಿಚಾರಣೆ ಮಾಡಲು ನಮ್ಮನ್ನು ಸಕ್ರಿಯಗೊಳಿಸುವ ಮೂಲಕ ನಿಮಗೆ ಉತ್ತಮ ವೆಬ್‌ಸೈಟ್ ಒದಗಿಸಲು ಕುಕೀಗಳು ನಮಗೆ ಸಹಾಯ ಮಾಡುತ್ತವೆ. ನಮ್ಮೊಂದಿಗೆ ಹಂಚಿಕೊಳ್ಳಲು ನೀವು ಆಯ್ಕೆಮಾಡಿದ ಡೇಟಾವನ್ನು ಹೊರತುಪಡಿಸಿ ನಿಮ್ಮ ಕಂಪ್ಯೂಟರ್‌ಗೆ ಅಥವಾ ನಿಮ್ಮ ಕುರಿತು ಯಾವುದೇ ಮಾಹಿತಿಗೆ ಯಾವುದೇ ರೀತಿಯಲ್ಲಿ ಕುಕೀಯು ನಮಗೆ ಪ್ರವೇಶವನ್ನು ನೀಡುವುದಿಲ್ಲ. ನೀವು ಕುಕೀಗಳನ್ನು ಸ್ವೀಕರಿಸಲು ಅಥವಾ ನಿರಾಕರಿಸಲು ಆಯ್ಕೆ ಮಾಡಬಹುದು. ಹೆಚ್ಚಿನ ವೆಬ್ ಬ್ರೌಸರ್‌ಗಳು ಸ್ವಯಂಚಾಲಿತವಾಗಿ ಕುಕೀಗಳನ್ನು ಸ್ವೀಕರಿಸುತ್ತವೆ, ಆದರೆ ನೀವು ಬಯಸಿದಲ್ಲಿ ಕುಕೀಗಳನ್ನು ನಿರಾಕರಿಸಲು ನಿಮ್ಮ ಬ್ರೌಸರ್ ಸೆಟ್ಟಿಂಗ್‌ಗಳನ್ನು ನೀವು ಸಾಮಾನ್ಯವಾಗಿ ಮಾರ್ಪಡಿಸಬಹುದು. ಇದರೊಂದಿಗೆ  ವೆಬ್‌ಸೈಟ್‌ನ ಸಂಪೂರ್ಣ ಪ್ರಯೋಜನವನ್ನು ಪಡೆದುಕೊಳ್ಳುವುದನ್ನು ತಡೆಯಬಹುದು.
