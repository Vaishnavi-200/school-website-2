                           1) ನಮ್ಮ ಪರವಾಗಿ ಯಾವುದೇ ಸೇವೆಗಳನ್ನು ನಿರ್ವಹಿಸುವ ಮೂರನೇ ವ್ಯಕ್ತಿಗಳೊಂದಿಗೆ ನಾವು ಮಾಹಿತಿಯನ್ನು ಹಂಚಿಕೊಳ್ಳುತ್ತೇವೆ. ನಾವು ನಮ್ಮ ವ್ಯಾಪಾರ ಪಾಲುದಾರರೊಂದಿಗೆ ಮಾಹಿತಿಯನ್ನು ಹಂಚಿಕೊಳ್ಳುತ್ತೇವೆ.
                           <br><br>
                           2) ಕಾನೂನನ್ನು ಅನುಸರಿಸಲು ಅಥವಾ ನಮ್ಮನ್ನು ನಾವು ರಕ್ಷಿಸಿಕೊಳ್ಳಲು ನಾವು ಭಾವಿಸಿದರೆ ನಾವು ಮಾಹಿತಿಯನ್ನು ಹಂಚಿಕೊಳ್ಳಬಹುದು. ನ್ಯಾಯಾಲಯದ ಆದೇಶಕ್ಕೆ ಪ್ರತಿಕ್ರಿಯಿಸಲು ಅಥವಾ ಯಾವುದೇ ಶಾಸನಬದ್ಧ ಅವಶ್ಯಕತೆಗಳನ್ನು ಅನುಸರಿಸಲು ನಾವು ಮಾಹಿತಿಯನ್ನು ಹಂಚಿಕೊಳ್ಳುತ್ತೇವೆ. ಸರ್ಕಾರಿ ಸಂಸ್ಥೆ ಅಥವಾ ತನಿಖಾ ಸಂಸ್ಥೆಯು ಅದನ್ನು ವಿನಂತಿಸಿದರೆ ನಾವು ಅದನ್ನು ಹಂಚಿಕೊಳ್ಳಬಹುದು. ಅಥವಾ ನಾವು ಸಂಭಾವ್ಯ ವಂಚನೆಯನ್ನು ತನಿಖೆ ಮಾಡುವಾಗ ನಾವು ಮಾಹಿತಿಯನ್ನು ಹಂಚಿಕೊಳ್ಳಬಹುದು.
